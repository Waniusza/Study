public class coloqum1 {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            for (int j = 2; j <= 10; j = j + 2) {
                System.out.print(i * 10 + j + " ");
            }
            System.out.println();
        }
    }
}
